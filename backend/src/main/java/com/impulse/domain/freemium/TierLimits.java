package com.impulse.freemium;

public class TierLimits {
    private int activeChallenges;
    private int monthlyValidations;
    private int coachingMessages;
    private int videoCalls;
    private int customTemplates;
    private int premiumThemes;
    private int referralBonuses;
    private int storageGB;
    private int apiCallsPerDay;

    public int getActiveChallenges() { return activeChallenges; }
    public void setActiveChallenges(int activeChallenges) { this.activeChallenges = activeChallenges; }
    public int getMonthlyValidations() { return monthlyValidations; }
    public void setMonthlyValidations(int monthlyValidations) { this.monthlyValidations = monthlyValidations; }
    public int getCoachingMessages() { return coachingMessages; }
    public void setCoachingMessages(int coachingMessages) { this.coachingMessages = coachingMessages; }
    public int getVideoCalls() { return videoCalls; }
    public void setVideoCalls(int videoCalls) { this.videoCalls = videoCalls; }
    public int getCustomTemplates() { return customTemplates; }
    public void setCustomTemplates(int customTemplates) { this.customTemplates = customTemplates; }
    public int getPremiumThemes() { return premiumThemes; }
    public void setPremiumThemes(int premiumThemes) { this.premiumThemes = premiumThemes; }
    public int getReferralBonuses() { return referralBonuses; }
    public void setReferralBonuses(int referralBonuses) { this.referralBonuses = referralBonuses; }
    public int getStorageGB() { return storageGB; }
    public void setStorageGB(int storageGB) { this.storageGB = storageGB; }
    public int getApiCallsPerDay() { return apiCallsPerDay; }
    public void setApiCallsPerDay(int apiCallsPerDay) { this.apiCallsPerDay = apiCallsPerDay; }
}
